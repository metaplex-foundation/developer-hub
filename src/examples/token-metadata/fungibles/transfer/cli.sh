# Transfer Tokens using the Metaplex CLI

# Transfer tokens to a destination address
# Arguments: <mintAddress> <amount> <destination>
mplx toolbox token transfer <mintAddress> <amount> <destinationAddress>

# Example: Transfer 100 tokens (with 9 decimals = 100_000_000_000 basis points)
mplx toolbox token transfer 7xKXtg2CW87d97TXJSDpbD5jBkheTqA83TZRuJosgAsU 100000000000 9WzDXwBbmkg8ZTbNMqUxvQRAyrZzDsGYdLVL9zYtAWWM
